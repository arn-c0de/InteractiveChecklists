def get_last_line(path: str) -> str | None:
    """Efficiently read the last complete line from a file by seeking from the end.
    
    This avoids reading the entire file, which is important for large JSONL files.
    Returns None if file is empty or only contains incomplete lines.
    
    Note: Opens file fresh each time to avoid stale cache issues.
    """
    try:
        with open(path, 'rb') as f:
            # Seek to end
            f.seek(0, os.SEEK_END)
            file_size = f.tell()
            
            if file_size == 0:
                return None
            
            # Read backwards in chunks to find last complete line
            # We read up to 64KB from the end, which should be enough for any JSON line
            chunk_size = min(65536, file_size)
            f.seek(-chunk_size, os.SEEK_END)
            
            # Read chunk and decode
            chunk = f.read(chunk_size)
            try:
                text = chunk.decode('utf-8', errors='ignore')
            except Exception:
                return None
            
            # Find last complete line (split by newline and take last non-empty)
            lines = text.split('\n')
            # Work backwards to find last non-empty line
            for i in range(len(lines) - 1, -1, -1):
                line = lines[i].strip()
                if line:
                    return line
            
            return None
    except Exception as e:
        logger.debug(f"Error reading last line: {e}")
        return None


# New feature: repeat the last line every X seconds
def repeat_last_line(path: str, host: str, port: int, session_mgr: 'SessionManager', interval=5.0, verbose=False, show_env=False,
                     handshake_port: int = None, bind_ip: str = '127.0.0.1'):
    """Send the last line of the file as a UDP datagram every <interval> seconds (ECDH only)."""
    # Use global timestamp state to prevent forwarding old/cached messages
    global _last_processed_timestamp
    if not os.path.exists(path):
        raise FileNotFoundError(path)

    # Create a UDP socket bound to handshake_port for handshake, send data to destination port
    sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    sock.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
    # SECURITY: Default bind to localhost only for safety
    # Use handshake_port if specified, otherwise use data port
    listen_port = handshake_port if handshake_port is not None else port
    sock.bind((bind_ip, listen_port))  # Bind to specified IP (default: localhost)
    # Use configured interval for socket timeout (non-blocking with fast response)
    sock.settimeout(max(0.01, interval))  # At least 10ms, use configured interval
    logger.info(f"🔐 Listening for handshakes on {bind_ip}:{listen_port}")
    if handshake_port is not None and handshake_port != port:
        logger.info(f"📤 Will send data to {host}:{port}")
    handshake_sock = sock  # Use same socket for handshake and data
    
    # Heartbeat tracking: send heartbeat every 30 seconds when no new data
    last_data_sent_time = time.time()
    HEARTBEAT_INTERVAL = 30.0  # seconds
    
    try:
        last_sent = None
        last_send_time = 0
        last_file_mtime = 0  # Track file modification time to detect changes
        cached_last_line = None  # Cache the last line to avoid unnecessary reads
        
        while True:
            # Check heartbeat FIRST (independent of data availability)
            current_time = time.time()
            if current_time - last_data_sent_time >= HEARTBEAT_INTERVAL:
                heartbeat_data = {
                    "type": "heartbeat",
                    "timestamp": datetime.now().isoformat() + 'Z',
                    "message": "No new flight data - connection alive"
                }
                heartbeat_json = json.dumps(heartbeat_data).encode('utf-8')
                
                # Send heartbeat to all active sessions
                heartbeat_sent = False
                for device_id in list(session_mgr.device_sessions.keys()):
                    if send_udp(heartbeat_json, host, port, sock,
                               session_mgr=session_mgr, device_id=device_id):
                        logger.info(f"💓 Heartbeat sent to {device_id[:8]}...")
                        heartbeat_sent = True
                
                if heartbeat_sent:
                    last_data_sent_time = current_time

            # In ECDH mode: check for incoming handshake messages
            if handshake_sock:
                try:
                    data, addr = handshake_sock.recvfrom(65535)  # Max UDP size

                    # SECURITY: Early validation before expensive parsing (DoS protection)
                    if not validate_handshake_message(data, addr):
                        # Message rejected by validation (rate limit, size, format, etc.)
                        continue

                    # Parse handshake message (PLAINTEXT - no PSK encryption)
                    try:
                        logger.debug(f"📦 Received {len(data)} bytes from {addr}")

                        # Handshake messages are sent in PLAINTEXT for proper ECDH
                        msg = safe_json_parse(data, max_size=_MAX_HANDSHAKE_MESSAGE_SIZE)
                        if not msg:
                            logger.debug("Failed to parse handshake message or message too large")
                            continue

                        # SECURITY: Avoid logging full message contents; show a short preview/truncated
                        try:
                            preview = json.dumps(msg)
                            logger.debug(f"📄 Full message (truncated): {preview[:200]}")
                        except Exception:
                            logger.debug("📄 Full message: <unserializable>")

                        logger.info(f"📨 Received {msg.get('type', 'Unknown')} from {addr[0]}")
                        logger.debug(f"📊 Message keys: {list(msg.keys())}")

                        if 'type' in msg and msg['type'] == 'ClientHello':
                            logger.info(f"📥 Received ClientHello from {addr}")
                            response = session_mgr.handle_client_hello(msg, addr)
                            # Send response in PLAINTEXT (no PSK encryption)
                            response_json = json.dumps(response).encode('utf-8')
                            handshake_sock.sendto(response_json, addr)
                            logger.info(f"📤 Sent ServerHello to {addr}")
                        elif 'type' in msg and msg['type'] == 'KeyConfirm':
                            logger.info(f"📥 Received KeyConfirm from {addr}")
                            response = session_mgr.handle_key_confirm(msg)
                            # Send response in PLAINTEXT (no PSK encryption)
                            response_json = json.dumps(response).encode('utf-8')
                            handshake_sock.sendto(response_json, addr)
                            if response.get('status') == 'ready':
                                logger.info(f"✅ Session established with device {msg.get('sessionId', 'unknown')[:8]}...")

                                # Send one immediate heartbeat to the newly established device so it has
                                # a fresh timestamp and avoids rejected/old-data behavior.
                                session_id = msg.get('sessionId')
                                device_id = find_device_for_session(session_mgr, session_id) if session_id else None
                                if device_id:
                                    heartbeat_data = {
                                        "type": "heartbeat",
                                        "timestamp": datetime.now().isoformat() + 'Z',
                                        "message": "Initial heartbeat after handshake"
                                    }
                                    heartbeat_json = json.dumps(heartbeat_data).encode('utf-8')
                                    try:
                                        if send_udp(heartbeat_json, host, port, handshake_sock,
                                                    session_mgr=session_mgr, device_id=device_id):
                                            logger.info(f"💓 Initial heartbeat sent to {device_id[:8]}...")
                                            # Update last_data_sent_time so we don't immediately send another heartbeat
                                            try:
                                                last_data_sent_time = time.time()
                                            except Exception:
                                                pass
                                        else:
                                            logger.warning(f"⚠️ Failed to send initial heartbeat to {device_id[:8]} (session may have expired)")
                                    except Exception as e:
                                        logger.warning(f"⚠️ Error sending initial heartbeat: {e}")
                    except Exception as decode_err:
                        # Not a handshake message or malformed, ignore
                        logger.debug(f"Failed to parse handshake: {decode_err}")
                        pass
                    except Exception as unexpected_err:
                        # Log unexpected errors for debugging
                        logger.warning(f"⚠️ Unexpected error parsing handshake: {unexpected_err}")
                except socket.timeout:
                    # No handshake message, continue
                    pass
                except Exception as e:
                    logger.debug(f"Handshake check error: {e}")
            
            # Send data at regular interval
            current_time = time.time()
            if current_time - last_send_time < interval:
                # Don't sleep - socket timeout handles the wait
                continue
            
            try:
                # Check if file has been modified since last read
                try:
                    file_mtime = os.path.getmtime(path)
                except OSError:
                    continue
                
                # Only read file if it has been modified OR we haven't cached anything yet
                if file_mtime != last_file_mtime or cached_last_line is None:
                    last_line = get_last_line(path)
                    if last_line:
                        cached_last_line = last_line
                        last_file_mtime = file_mtime
                        logger.debug(f"📄 Updated cached line from file (mtime: {file_mtime})")
                    else:
                        continue
                else:
                    # Use cached line (file hasn't changed)
                    last_line = cached_last_line
                    logger.debug("📦 Using cached last line (file unchanged)")
                
                if not last_line:
                    continue
                    
                jsonpart = extract_json_from_line(last_line)
                if not jsonpart:
                    continue

                # Validate that the extracted substring is valid JSON before sending
                parsed = safe_json_parse(jsonpart, max_size=_MAX_DATA_MESSAGE_SIZE)
                if parsed is None:
                    continue

                # Timestamp validation: reject old/cached messages
                try:
                    cur_ts = parsed.get('timestamp')
                except Exception:
                    cur_ts = None

                if not _timestamp_is_newer(cur_ts, _last_processed_timestamp):
                    logger.warning(f"⚠️ REJECTED OLD DATA: timestamp={cur_ts} (last={_last_processed_timestamp}) - preventing position reset!")
                    continue

                # Accept and record timestamp
                if cur_ts is not None:
                    logger.info(f"✅ Accepted data with timestamp={cur_ts}")
                    _last_processed_timestamp = cur_ts

                # Send to all devices with active sessions (ECDH mode)
                sent_count = 0
                for device_id, session_id in list(session_mgr.device_sessions.items()):
                    # Get device info for logging
                    device = session_mgr.authorized_devices.get(device_id)
                    device_name = device.name if device else device_id[:8]

                    if send_udp(jsonpart.encode('utf-8'), host, port, sock,
                               session_mgr=session_mgr, device_id=device_id):
                        sent_count += 1
                    else:
                        logger.warning(f"⚠️ Failed to send to {device_name} (session may have expired)")
                sent = sent_count > 0
                
                # Update last data sent time on successful send
                if sent:
                    last_data_sent_time = current_time
                
                last_send_time = current_time
                if verbose or show_env:
                    ts = time.strftime('%Y-%m-%d %H:%M:%S', time.localtime())
                    if sent:
                        if show_env:
                            try:
                                parsed = safe_json_parse(jsonpart, max_size=_MAX_DATA_MESSAGE_SIZE)
                                if parsed is not None:
                                    env = parsed.get('environment', {})
                                    temp = parsed.get('temperature', env.get('temperature'))
                                    pres = parsed.get('pressure', env.get('pressure'))
                                    wind = parsed.get('wind') or {'speed': env.get('windSpeed'), 'direction': env.get('windDirection')}
                                    wspeed = wind.get('speed')
                                    wdir = wind.get('direction')
                                    print(f"{ts} REPEAT {host}:{port} temp={temp}C pres={pres} wind={wspeed}@{wdir}")
                                else:
                                    print(f"{ts} REPEAT {host}:{port} {jsonpart}")
                            except Exception:
                                print(f"{ts} REPEAT {host}:{port} {jsonpart}")
                        else:
                            print(f"{ts} REPEAT {host}:{port} {jsonpart}")
                    else:
                        print(f"{ts} ERROR {host}:{port} {jsonpart}")
            except Exception as e:
                print(f"Error reading/sending: {e}", file=sys.stderr)
    finally:
        sock.close()
        # handshake_sock is same as sock in ECDH mode, don't close twice


def write_live_command(interval_ms: int, scripts_dir: str = None):
    """
    Writes a live command file that Export.lua can read to update interval dynamically.
    This allows changing the interval WITHOUT restarting DCS!
    """
    interval_seconds = interval_ms / 1000.0

    # Try to find DCS Scripts folder if not provided
    if not scripts_dir:
        scripts_dir = find_dcs_scripts_folder()

    if not scripts_dir:
        logger.warning("Cannot write live command: DCS Scripts folder not found")
        return

    command_path = os.path.join(scripts_dir, 'forwarder_command.json')

    try:
        command_data = {
            "updateInterval": interval_seconds,
            "timestamp": datetime.now().isoformat()
        }
        with open(command_path, 'w', encoding='utf-8') as f:
            json.dump(command_data, f, indent=2)
        logger.info(f"✅ Live command sent: UPDATE_INTERVAL → {interval_seconds}s (DCS will update within 2 seconds)")
    except Exception as e:
        logger.warning(f"Failed to write live command: {e}")


def update_export_lua_interval(interval_ms: int, lua_path: str):
    """
    Updates the UPDATE_INTERVAL in the specified Export.lua file.
    """
    if not os.path.exists(lua_path):
        logger.warning(f"Export.lua not found at {lua_path}, cannot update interval.")
        return

    interval_seconds = interval_ms / 1000.0

    try:
        with open(lua_path, 'r', encoding='utf-8') as f:
            lines = f.readlines()

        updated = False
        new_lines = []
        # Regex to find 'local UPDATE_INTERVAL = 0.2' (or other numbers)
        pattern = re.compile(r"(local\s+UPDATE_INTERVAL\s*=\s*)[\d.]+")

        for line in lines:
            new_line, count = pattern.subn(r"\g<1>" + str(interval_seconds), line)
            if count > 0:
                new_lines.append(new_line)
                updated = True
            else:
                new_lines.append(line)

        if updated:
            # Safely write back to the file
            temp_path = lua_path + '.tmp'
            with open(temp_path, 'w', encoding='utf-8') as f:
                f.writelines(new_lines)
            os.replace(temp_path, lua_path)
            logger.info(f"Updated UPDATE_INTERVAL in {os.path.basename(lua_path)} to {interval_seconds} seconds (requires DCS restart).")


    except Exception as e:
        logger.error(f"Failed to update {os.path.basename(lua_path)}: {e}")


