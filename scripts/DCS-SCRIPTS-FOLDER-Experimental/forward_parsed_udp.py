def find_dcs_scripts_folder() -> str | None:
    """Attempt to locate the user's DCS "Saved Games/.../DCS/Scripts" folder.

    Returns absolute path to Scripts folder or None if not found.
    """
    try:
        # Typical Windows location under user's profile
        home = os.path.expanduser("~")
        candidate = os.path.join(home, "Saved Games", "DCS", "Scripts")
        if os.path.isdir(candidate):
            return candidate

        # Look for directories under "Saved Games" that start with 'DCS'
        saved_games = os.path.join(home, "Saved Games")
        if os.path.isdir(saved_games):
            for entry in os.listdir(saved_games):
                if entry.lower().startswith('dcs'):
                    p = os.path.join(saved_games, entry, "Scripts")
                    if os.path.isdir(p):
                        return p

        # Fallback: search current working directory tree for a 'Scripts' folder that contains export.lua
        cwd = os.getcwd()
        for root, dirs, files in os.walk(cwd):
            if 'Scripts' in dirs:
                scripts_dir = os.path.join(root, 'Scripts')
                if os.path.exists(os.path.join(scripts_dir, 'export.lua')):
                    return scripts_dir
    except Exception:
        pass
    return None


def find_export_file(scripts_dir: str) -> str | None:
    """Find the most recent export JSON/JSONL file generated by export.lua or similar.

    Looks for common filenames and returns the newest match or None.
    """
    if not scripts_dir or not os.path.isdir(scripts_dir):
        return None

    patterns = [
        'player_aircraft_parsed.jsonl',
        'export.json',
        'export.jsonl',
        'export_*.json',
        'export_*.jsonl',
        'player_*.json',
        'player_*.jsonl',
        '*.export.json',
        '*.export.jsonl'
    ]

    matches = []
    for pat in patterns:
        globpat = os.path.join(scripts_dir, pat)
        for p in glob.glob(globpat):
            if os.path.isfile(p):
                matches.append(p)

    if not matches:
        # Recursively search one level deeper just in case
        for sub in os.listdir(scripts_dir):
            subp = os.path.join(scripts_dir, sub)
            if os.path.isdir(subp):
                for pat in patterns:
                    globpat = os.path.join(subp, pat)
                    for p in glob.glob(globpat):
                        if os.path.isfile(p):
                            matches.append(p)

    if not matches:
        return None

    # Return the most recently modified match
    matches.sort(key=lambda p: os.path.getmtime(p), reverse=True)
    return matches[0]


